package TextInheritance01;

public class Student {
	String name;
	String hakbun;
	String telephone;
	String address;
	String department;
	//이름 : Name
	//학번 : hakbun
	//전화번호 : telephone
	//주소 : address
	//학과 : department
	
	void study() {
		System.out.println("공부합니다.");
	}
	void eat() {
		System.out.println("식사합니다.");
	}
	void test() {
		System.out.println("시헙을 봅니다.");
	}
	void club() {
		System.out.println("동아리활동을 합니다.");
	}
	//메소드
	//공부하다 : study()
	//식사하다 : eat()
	//시험보다 : test()
	//동아리활동하다 : club()

}
